package model.world.initialization;

/**
 * 
 * Different modalities of initialization of world.
 *
 */
public enum Modality {
    /**
     * Modality with only one cell with only photosynthesis genes.
     */
    SINGLE_PHOTOSYNTHESIS_CELL,
    /**
     * Modality with a lot of cells with random generated genomes..
     */
    RANDOMS_GENOME_CELLS;
}
